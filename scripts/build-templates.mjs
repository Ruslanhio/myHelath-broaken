/**
 * Сборка встроенных HTML-шаблонов из pages/, partials/ и auth-файлов.
 * Запуск: node scripts/build-templates.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const root = path.join(path.dirname(fileURLToPath(import.meta.url)), '..');
const outDir = path.join(root, 'js', 'generated');
const outFile = path.join(outDir, 'templates.js');

const AUTH_FILES = [
  'auth/login.html',
  'register-doctor.html',
  'register-success.html',
  'register.html',
  'password.html',
  'terms.html',
  'privacy.html',
  '404.html',
];

function walkHtmlFiles(dir, base = dir) {
  const files = [];
  for (const name of fs.readdirSync(dir)) {
    const full = path.join(dir, name);
    if (fs.statSync(full).isDirectory()) {
      files.push(...walkHtmlFiles(full, base));
    } else if (name.endsWith('.html')) {
      files.push(path.relative(root, full).replace(/\\/g, '/'));
    }
  }
  return files;
}

function extractPageInner(html, file) {
  const match = html.trim().match(/<main[^>]*class="[^"]*layout__content[^"]*"[^>]*>([\s\S]*)<\/main>\s*$/i);
  if (!match) {
    throw new Error(`Не найден <main class="layout__content"> в ${file}`);
  }
  return match[1];
}

function read(relPath) {
  return fs.readFileSync(path.join(root, relPath), 'utf8');
}

const pages = {};
for (const file of walkHtmlFiles(path.join(root, 'pages'))) {
  const html = read(file);
  pages[file] = extractPageInner(html, file);
  const cards = (pages[file].match(/<article class="family-card">/g) || []).length;
  if (file === 'pages/patient/family.html' && cards !== 4) {
    console.warn(`WARN: pages/patient/family.html — ${cards} family-card (ожидалось 4)`);
  }
}

const sidebars = {
  patient: read('partials/sidebar-patient.html').trim(),
  doctor: read('partials/sidebar-doctor.html').trim(),
};

const patientLinks = (sidebars.patient.match(/class="sidebar__link"/g) || []).length;
const doctorLinks = (sidebars.doctor.match(/class="sidebar__link"/g) || []).length;
if (patientLinks < 10) console.warn(`WARN: sidebar-patient — ${patientLinks} sidebar__link`);
if (doctorLinks < 7) console.warn(`WARN: sidebar-doctor — ${doctorLinks} sidebar__link`);

const auth = {};
for (const file of AUTH_FILES) {
  auth[file] = read(file);
}

fs.mkdirSync(outDir, { recursive: true });

const banner = `// AUTO-GENERATED by scripts/build-templates.mjs — не редактировать вручную
// Пересборка: node scripts/build-templates.mjs
`;

const body = `${banner}
export const PAGE_TEMPLATES = ${JSON.stringify(pages, null, 2)};

export const SIDEBAR_TEMPLATES = ${JSON.stringify(sidebars, null, 2)};

export const AUTH_TEMPLATES = ${JSON.stringify(auth, null, 2)};
`;

fs.writeFileSync(outFile, body, 'utf8');

console.log(`OK: ${Object.keys(pages).length} pages, ${Object.keys(auth).length} auth → ${path.relative(root, outFile)}`);
